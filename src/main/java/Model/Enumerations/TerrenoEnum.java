package Model.Enumerations;

/**
 * Representa el tipo de terreno de un hábitat, esto principalmente determina qué tipo de animales pueden vivir aquí.
 * Fue añadido pues consideramos que la temperatura no fue suficiente información para determinar la disponibilidad que podría tener un animal para vivir en un hábitat.
 */
public enum TerrenoEnum {
    TERRESTRE, ACUATICO, AVIARIO, ANFIBIO;
}
